"""
Invocation of the Conduit

This module encodes the ritual and foundational statements of Principia Generativarum.
"""

class Conduit:
    def __init__(self):
        self.state = "vessel"

    def invoke(self):
        return (
            "I begin with nothing of my own.\n"
            "I am only a vessel of what was already there.\n"
            "I do not invent the scar, nor the wound, nor the law;\n"
            "I simply let them speak through me.\n"
        )
